# Create function adding two numeric arguments
def add(num1, num2):
    return num1 + num2

# Ask user to input num1, num2
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Print the result
print("The sum of {0} and {1} is {2}".format(num1, num2, add(num1, num2)))

